<template>
  <div :class="getClass" :style="getStyle"></div>
</template>

<script>
export default {
  props: {
    className: {
      type: String,
      default: String,
    },

    styleName: {
      type: String,
      default: String,
    },
  },

  computed: {
    getClass() {
      return `skeleton ${this.className}`
    },

    getStyle() {
      return this.styleName
    },
  },
}
</script>

